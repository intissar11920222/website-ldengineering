                $(".plan_intervention").click(function () {

                    $id = $(this).attr('id');
                    $num = $id.substring(4, 6);

                    for ($i = 1; $i <= 9; $i++) {
                        if ($i == $num) {
                            $("#head" + $num).addClass("active");
                            $("#step" + $num).show();
                        } else {
                            $("#head" + $i).removeClass("active");
                            $("#step" + $i).hide();
                        }
                    }

                });






