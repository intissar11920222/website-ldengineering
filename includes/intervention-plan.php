                        <div class="tab-pane active" id="step1" hidden="hidden">
                            <h3>Définition d'objectifs</h3>
                            <p style="text-align: justify;">Comme dans tout projet, la première étape consiste à définir les objectifs. On parle trop souvent de définition du besoin. Je pense qu’il est plus pertinent d’utiliser le terme de définition d’objectifs. En effet, vous pouvez avoir besoin d’une application mobile (besoin), mais cela ne nous informe en rien de vos objectifs. À l’inverse, vous pouvez vouloir accroître votre visibilité (objectif), et l’application mobile se présente comme une solution parmi tant d’autres.</p>

                        </div>
                        <div class="tab-pane" id="step2" hidden="hidden">
                            <h3>Recherche de solutions</h3>
                            <p style="text-align: justify;">Une fois vos objectifs définis, vous devez rechercher des solutions permettant d’y répondre. Si vous souhaitez accroître votre visibilité, vous avez une multitude de solutions qui s’offrent à vous : site internet, référencement, médias sociaux, newsletter, etc. Vous devez donc ici lister l’ensemble des solutions possibles et les classer en fonction de vos critères de choix. Une fois listées, vous pouvez ainsi choisir votre solution.</p>

                        </div>
                        <div class="tab-pane" id="step3" hidden="hidden">
                            <h3>Cahier des charges fonctionnel</h3>
                            <p style="text-align: justify;">Maintenant que votre solution est choisie, vous devez définir le cahier des charges fonctionnel. Il s’agit d’un document qui permet de formaliser avec précision le besoin du demandeur. En effet, le CDCF est un tableau de bord qui définit le projet et détaille les conditions dans lesquelles il doit être réalisé ; il décrit l'ensemble des caractéristiques attendues des fonctions de service. Autrement dit, vous devez lister, définir et détailler l’ensemble des fonctions auxquelles votre solution devra répondre. Vous souhaitez rechercher de la visibilité en mettant en place votre site internet, celui-ci devra répondre à des fonctions telles que la prise de contact, la demande de devis, l’inscription à une newsletter, etc.</p>

                        </div>
                        <div class="tab-pane" id="step4" hidden="hidden">
                            <h3>Cahier des charges technique</h3>
                            <p style="text-align: justify;">Le cahier des charges technique est une réponse à l’expression des besoins du client (le cahier des charges fonctionnel).
                                Il va établir les propositions techniques, les modalités d’exécution du projet, son coût et sa durée.</p>

                        </div>
                        <div class="tab-pane" id="step5" hidden="hidden">
                            <h3>Conception préliminaire</h3>
                            <p style="text-align: justify;">La phase de conception préliminaire consiste à définir le meilleur concept possible qui répond aux besoins du client, c’est ici que nous allons réaliser dans les grandes lignes la maquette visuelle (web design) de votre projet. Dans cette étape les principales composantes à prendre en compte sont l’ergonomie et l’expérience utilisateur que vous souhaitez offrir.</p>

                        </div>
                        <div class="tab-pane" id="step6" hidden="hidden">
                            <h3>Conception détaillée</h3>
                            <p style="text-align: justify;">C’est ici que l’on construit le projet dans son intégralité et dans le respect des cahiers des charges.</p>

                        </div>
                        <div class="tab-pane" id="step7" hidden="hidden">
                            <h3>Tests / Recettes</h3>
                            <p style="text-align: justify;">La phase de test intervient ensuite pour identifier les bugs potentiels et vérifier le bon respect des cahiers de charges. Dans le milieu du web, la phase de test est souvent réalisée avec des “beta-testeurs” qui testent et font remonter les bugs éventuels qu’ils rencontrent en échange d’une utilisation privilégiée avant le lancement du projet.</p>

                        </div>
                        <div class="tab-pane" id="step8" hidden="hidden">
                            <h3>Déploiement</h3>
                            <p style="text-align: justify;">La phase de déploiement n’est ni plus ni moins la phase de lancement / mise en ligne du projet. Trop souvent les projets web sont lancés alors qu’ils ne sont pas finalisés à 100%. Il faut bien s’assurer que l’ensemble des bugs identifiés au préalables ont bien été rectifiés</p>

                        </div>
                        <div class="tab-pane" id="step9" hidden="hidden">
                            <h3>Suivi / Pilotage</h3>
                            <p style="text-align: justify;">La phase de suivi consiste à mesurer périodiquement l’efficacité du projet par rapport aux objectifs initialement fixés, et à mettre en place des actions correctives pour réellement “piloter” le projet.</p>

